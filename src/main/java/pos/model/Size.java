package pos.model;

public class Size {
    private String sizeid;

    private String no;

    private String size;

    private Boolean otherflag;

    private Boolean useflag;

    private String memo;

    private String uk;

    private String usa;

    private String sizeEshop;

    public String getSizeid() {
        return sizeid;
    }

    public void setSizeid(String sizeid) {
        this.sizeid = sizeid;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public Boolean getOtherflag() {
        return otherflag;
    }

    public void setOtherflag(Boolean otherflag) {
        this.otherflag = otherflag;
    }

    public Boolean getUseflag() {
        return useflag;
    }

    public void setUseflag(Boolean useflag) {
        this.useflag = useflag;
    }

    public String getMemo() {
        return memo;
    }

    public void setMemo(String memo) {
        this.memo = memo;
    }

    public String getUk() {
        return uk;
    }

    public void setUk(String uk) {
        this.uk = uk;
    }

    public String getUsa() {
        return usa;
    }

    public void setUsa(String usa) {
        this.usa = usa;
    }

    public String getSizeEshop() {
        return sizeEshop;
    }

    public void setSizeEshop(String sizeEshop) {
        this.sizeEshop = sizeEshop;
    }
}