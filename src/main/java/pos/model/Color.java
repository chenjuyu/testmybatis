package pos.model;

public class Color {
    private String colorid;

    private String no;

    private String color;

    private Boolean useflag;

    private String memo;

    private Boolean stopflag;

    private String colorEshop;

    private String helpcode;

    private String wyCode;

    public String getColorid() {
        return colorid;
    }

    public void setColorid(String colorid) {
        this.colorid = colorid;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Boolean getUseflag() {
        return useflag;
    }

    public void setUseflag(Boolean useflag) {
        this.useflag = useflag;
    }

    public String getMemo() {
        return memo;
    }

    public void setMemo(String memo) {
        this.memo = memo;
    }

    public Boolean getStopflag() {
        return stopflag;
    }

    public void setStopflag(Boolean stopflag) {
        this.stopflag = stopflag;
    }

    public String getColorEshop() {
        return colorEshop;
    }

    public void setColorEshop(String colorEshop) {
        this.colorEshop = colorEshop;
    }

    public String getHelpcode() {
        return helpcode;
    }

    public void setHelpcode(String helpcode) {
        this.helpcode = helpcode;
    }

    public String getWyCode() {
        return wyCode;
    }

    public void setWyCode(String wyCode) {
        this.wyCode = wyCode;
    }
}