package pos.model;

public class Brandcolor {
    private String colorid;

    private String brandid;

    private String no;

    private String color;

    private Boolean userflag;

    private String suppliercolorcode;

    public String getColorid() {
        return colorid;
    }

    public void setColorid(String colorid) {
        this.colorid = colorid;
    }

    public String getBrandid() {
        return brandid;
    }

    public void setBrandid(String brandid) {
        this.brandid = brandid;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Boolean getUserflag() {
        return userflag;
    }

    public void setUserflag(Boolean userflag) {
        this.userflag = userflag;
    }

    public String getSuppliercolorcode() {
        return suppliercolorcode;
    }

    public void setSuppliercolorcode(String suppliercolorcode) {
        this.suppliercolorcode = suppliercolorcode;
    }
}